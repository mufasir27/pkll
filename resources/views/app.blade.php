                                    <p>Tentang Pemain

Marc Anthony Klok, lahir di Amsterdam, Belanda pada 20 April 1993, saat ini Klok adalah kapten dan pilar penting PERSIB. Sebelum menjadi Warga Negara Indonesia (WNI), Klok memulai karier profesionalnya di Belanda, tepatnya di Jong FC Utrecht.

Setelah menimba ilmu di akademi Jong FC Utrecht, Klok mendapatkan kesempatan untuk bermain di kompetisi yang lebih tinggi. Pada tahun 2013, ia bergabung dengan Ross County, sebuah klub yang berkompetisi di Liga Utama Skotlandia. Pengalaman bermain di Skotlandia memberikan Klok tantangan baru dan membuatnya lebih matang sebagai pemain.

Tak puas dengan pencapaiannya, Klok kemudian melanjutkan kariernya ke Bulgaria dengan bergabung bersama Cherno More Varna. Disana, Klok berhasil meraih beberapa gelar, termasuk Piala Bulgaria dan Piala Super Bulgaria.

Setelah beberapa musim berkarier di Eropa, Klok memutuskan untuk memulai petualangan baru di Indonesia. Ia bergabung dengan PSM Makassar dan langsung menjadi pemain kunci. Adaptasi dengan iklim dan gaya bermain yang berbeda di Indonesia tidak menyulitkan Klok. Performa nya membuat namanya semakin dikenal di kancah sepak bola Indonesia dan kemudian bergabung Persija Jakarta.

Kepindahan Klok ke PERSIB pada tahun 2021 menjadi salah satu transfer paling menarik di Liga 1. Sejak bergabung dengan Pangeran Biru, Klok langsung menjadi jantung permainan tim. Kepemimpinannya yang kuat dan kemampuannya dalam mengatur ritme permainan membuat Klok menjadi kapten tim yang sangat berpengaruh.

Musim 2023/2024 menjadi musim yang paling mengesankan bagi Marc Klok bersama PERSIB. 

Klok bermain 2.792’ menit di Liga 1 musim lalu. Waktunya yang luas di lapangan menggarisbawahi pentingnya bagi tim dan penampilannya yang konsisten sepanjang musim.

Dengan catatan 5 gol dan 7 assist dari 34 pertandingan, Klok berhasil membawa PERSIB meraih gelar juara Liga 1. Performanya yang konsisten sepanjang musim membuatnya terpilih sebagai Best XI of the season Liga 1 2023/2024.</p>
