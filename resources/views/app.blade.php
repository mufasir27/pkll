                                    <p>Tentang Pemain

Mohamad Edo Febriansah memulai karier profesionalnya di PS Bengkulu pada tahun 2017, di mana ia menunjukkan bakatnya dengan mencetak gol dan memberikan kontribusi signifikan dalam permainan tim. Kehadirannya di PS Bengkulu menjadi langkah awal yang penting dalam menjelajahi sepak bola profesional di Indonesia.

Edo juga memiliki pengalaman internasional bersama tim nasional Indonesia. Ia telah bermain untuk tim U-19 dan tim senior, memberikan kontribusi dalam berbagai pertandingan internasional. Dengan dedikasi dan kerja kerasnya, Edo Febriansah terus berusaha memberikan yang terbaik bagi klub dan negaranya.

Pada tahun 2018, Edo Febriansah bergabung dengan Persiwa Wamena, sebuah klub yang berlaga di Liga 2 Indonesia. Di Persiwa, Edo tampil impresif dengan mencatatkan 20 penampilan dan mencetak 6 gol. Penampilannya yang konsisten dan kemampuannya dalam bertahan serta menyerang membuatnya menjadi salah satu pemain kunci di tim tersebut.

Setahun kemudian, pada tahun 2019, Edo pindah ke Persik Kediri. Di Persik, ia menandatangani kontrak satu tahun dan langsung memberikan dampak positif bagi tim. Edo memainkan peran vital dalam membawa Persik Kediri menjuarai Liga 2 2019 dan berhasil promosi ke Liga 1.

Pada tahun 2020, Edo bergabung dengan Persita Tangerang dan tetap konsisten meskipun musim terganggu oleh pandemi COVID-19. Setelah dua musim sukses di Persita, Edo pindah ke RANS Nusantara FC pada tahun 2022. Di klub barunya, ia mendapat total 2.168 menit bermain dalam 25 menit pertandingan dan torehan 5 gol.

Resmi bergabung dengan Persib Bandung 2023, dengan kontrak dua tahun. Kehadirannya diharapkan dapat memperkuat lini pertahanan dan memberikan opsi serangan dari sisi kiri. 

Di musim 2023/2024 bersama Persib Bandung, Edo mencatatkan 1510  menit bermain dengan torehan 2 gol. Dilain sisi ia bersama rekan lain berhasil mengantarkan Persib menjuarai Champion Series BRI Liga 1. Prestasi mengesankan bagi seorang Edo yang mudah beradaptasi dan berkontribusi banyak bagi Persib Bandung. Hal ini membuat Edo kembali dipercaya untuk memperkuat PERSIB hingga akhir musim 2024/2025.

